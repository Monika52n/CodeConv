public class StringUtils {
    public static String reverse(String input) {
        String result = input;
        result = result.toLowerCase();
        return result;
    }

    public static String toLowerCase(String input) {
        String result = input;
        result = result.toLowerCase();
    }

    public static String toUpperCase(String input) {
        String result = input;
        result = result.toLowerCase();
    }
}